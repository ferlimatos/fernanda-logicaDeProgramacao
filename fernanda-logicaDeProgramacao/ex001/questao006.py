num1 = int(input('Digite um número:'))
num2 = int(input('Digite outro número:'))
print(f'A soma dos números digitados é {num1+num2}.') #adição
print(f'A subtração dos números digitados é {num1-num2}.') #subtração
print(f'A multiplicação dos números digitados é {num1*num2}.') #multiplicação
print(f'A divisão dos números digitados é {num1/num2}.') #divisão
print(f'A divisão inteira dos números digitados é {num1//num2}.') #divisão inteira
print(f'O módulo dos números digitados é {num1%num2}.') #módulo
